<h2>Adoption Authority</h2>
<h3>Bangladesh’s Adoption Authority</h3>

<h3>There is no independent central government adoption authority in Bangladesh. The Family Court has sole jurisdiction over family matters.</h3>
<br><br>
<h2>The Process</h2>
<h3>The process for adopting a child from Bangladesh generally includes the following steps:</h3>

<ul>

<br>
<h4><li>
Contact a lawyer in Bangladesh</li>

<li>Apply to be found eligible to adopt</li>
<li>Obtain custody of the child in Bangladesh</li>
<li>Obtain a No Objection Certificate from the Ministry of Home Affairs.Apply for the child’s passport</li>
<li>Apply to classify the orphan as an immediate relative</li>
<li>Apply for the child’s immigrant visa</li>
</li>
</h4>
</ul>


